class UserMenus:

    @staticmethod
    def string():
        """
        Prints a dashed string
        """
        return print('---------------------------------------------------------------------------------------------')

    @staticmethod
    def n_string():
        """
        Prints a dashed string jumping a line at the beginning
        """
        return print('\n---------------------------------------------------------------------------------------------')

    @staticmethod
    def string_n():
        """
        Prints a dashed string jumping a line at the end
        """
        return print('---------------------------------------------------------------------------------------------\n')







